package br.itarocha.spring.util;

public class LinhaPesquisa {
	
	private String campo;
	private String operador;
	private String expressao;
	private String complemento;
	private String expressaolista;
	private String complementolista;
	private String tipo;
	
	public String getCampo() {
		return campo;
	}
	public void setCampo(String campo) {
		this.campo = campo;
	}
	public String getOperador() {
		return operador;
	}
	public void setOperador(String operador) {
		this.operador = operador;
	}
	public String getExpressao() {
		return expressao;
	}
	public void setExpressao(String expressao) {
		this.expressao = expressao;
	}
	public String getComplemento() {
		return complemento;
	}
	public void setComplemento(String complemento) {
		this.complemento = complemento;
	}
	public String getExpressaolista() {
		return expressaolista;
	}
	public void setExpressaolista(String expressaolista) {
		this.expressaolista = expressaolista;
	}
	public String getComplementolista() {
		return complementolista;
	}
	public void setComplementolista(String complementolista) {
		this.complementolista = complementolista;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	

}
